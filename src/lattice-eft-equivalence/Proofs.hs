Companion code for Part IV, "From Lattice Models to Effective Field Theories:
Stabilization and the Invertible Condensed Phase Spectrum". Where
"Properties" samples the paper's claims randomly (QuickCheck), this module
proves them: each section gives an explicit equational derivation in a
Haddock comment -- mirroring the paper's own proof -- and an executable,
deterministic (non-random) check of the concrete instances and finite ranges
the derivation covers.

  * __Section 1__ (Theorem IV-A \/ Theorem 4.2, @thm:groth@): @gamma@ is a
    monoid homomorphism and every group-completion element decomposes as
    @gamma a - gamma b@, both /definitionally/; the toy monoid's relation
    @t+a=t@ forces any homomorphism out of it to send @a@ to @0@; and the
    induced map's well-definedness and factorization (Theorem 4.2(iii),
    Proposition 4.4) are exhibited on concrete witnesses, together with a
    demonstration that the well-definedness hypothesis is not vacuous: drop
    it and @induced@ becomes genuinely ill-defined.
  * __Section 2__ (Example 4.5, @ex:noncanc@): @K(\<a,t|t+a=t\>)@ is isomorphic
    to @Z@, generated by @[t]@, with @[a]=0@ -- established (on the
    generator-restricted carrier and a finite witnessed range) from the universal property itself,
    via the induced homomorphism to @Z@ that reads off the @t@-exponent,
    rather than from case analysis on normal forms.
  * __Section 3__ (Proposition 5.8, @prop:bott@): Bott periodicity and
    antidiagonal invariance are traced to their algebraic mechanisms --
    periodicity to the periodicity of integer @mod@, antidiagonal invariance
    to the plain arithmetic identity @(s+1)-(d+1)=s-d@ -- and Table 1's five
    named landmark entries are re-derived with the index arithmetic shown
    explicitly.
